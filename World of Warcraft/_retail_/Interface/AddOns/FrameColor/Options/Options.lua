function addon:UpdateSearch(subgroup, value)
    if value then
        search = value
        local input = string_lower(value)
        for k,v in pairs (options.args[subgroup].args) do
            if v.args.enabled then
                local name = string_lower(v.args.enabled.name)
                if not name:match(input) then
                    v.guiHidden = true
                end
            end
        end
    else
        search = ""
        for k,v in pairs (options.args[subgroup].args) do
            if not v.hidden then
                v.guiHidden = false
            end
        end
    end
end

